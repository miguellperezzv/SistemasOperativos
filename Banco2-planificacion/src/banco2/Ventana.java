    private void btnAtenderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtenderActionPerformed
        // TODO add your handling code here:
        String info = "";
        if (fila.vacio()) {
            JOptionPane.showMessageDialog(null, "No hay m√°s clientes en la fila!");
            info = "****Personas en la fila  *** \n"
                    + "NOMBRE \t TRANSACCIONES  \n";

            txtAreaUsuarios.setText(info);

        } else {
            Fila temp = fila.atender(info);
            fila = temp;
        }

        this.actualizarTextArea();
        lbl_info.setText(info);
        fila.mostrarLista();
    }//GEN-LAST:event_btnAtenderActionPerformed

    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:
        String nombre = txtNombre.getText();
        String info = "";
        int tx = Integer.parseInt(txtTareas.getText());
        boolean prioridad = false;
        if (checkboxPrioridad.isSelected()) {
            prioridad = true;
        }

        //Nodo usuario = new Nodo( nombre, tx, prioridad);
        if (prioridad == true) {
            Nodo n = new Nodo(nombre, tx, prioridad);
            fila = fila.ponerAdelante(n);
            info = " Registrado con prioridad";
        } else {
            fila = fila.insertar(nombre, tx, prioridad);
            info = " Registrado en cola";
        }

        fila.mostrarLista();
        this.actualizarTextArea();
        lbl_info.setText(info);
    }//GEN-LAST:event_btnRegistrarActionPerformed

