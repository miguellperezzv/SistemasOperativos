    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        // TODO add your handling code here:
        //System.out.println("HOLASSSSSSSSSSSSSS");
        
        String tipoAlg = this.cmbAlgoritmo.getSelectedItem().toString();
        System.out.println("TIPOALG" + tipoAlg);
        if (tipoAlg.equals("1. Round Robin")) {

            this.round.quantum = 4;
            String llegada = this.txtLLegada.getText();
            String rafaga = this.txtRafaga.getText();

            int l = 0;
            int r = (int) (Math.random() * 10 + 1);

            if (!llegada.isEmpty()) {
                l = Integer.parseInt(llegada);
            }
            if (!rafaga.isEmpty()) {
                r = Integer.parseInt(rafaga);
            }

            this.round.insertar(l, r);
            this.txtArearobin.setText(this.round.listar());

        } else if (tipoAlg.equals("2. FIFO")) {
            String llegada = this.txtLLegada.getText();
            String rafaga = this.txtRafaga.getText();

            int l = 0;
            int r = (int) (Math.random() * 10 + 1);

            if (!llegada.isEmpty()) {
                l = Integer.parseInt(llegada);
            }
            if (!rafaga.isEmpty()) {
                r = Integer.parseInt(rafaga);
            }

            this.fifo.insertar(l, r);
            this.txtAreafifo.setText(this.fifo.listar());
        } else if (tipoAlg.equals("3. SJF")) {
            String llegada = this.txtLLegada.getText();
            String rafaga = this.txtRafaga.getText();

            int l = 0;
            int r = (int) (Math.random() * 10 + 1);

            if (!llegada.isEmpty()) {
                l = Integer.parseInt(llegada);
            }
            if (!rafaga.isEmpty()) {
                r = Integer.parseInt(rafaga);
            }

            this.sjf.insertar(l, r);
            this.txtAreasjf.setText(this.sjf.listar());

            

        }
        
        if (ejecutando) {
                round.setAlert(tipoAlg);
                round.calcularRoundTiempos();
                this.txtArearobin.setText(round.listar());

                fifo.setAlert(tipoAlg);
                fifo.calcularTiempos();
                this.txtAreafifo.setText(fifo.listar());

                sjf.setAlert(tipoAlg);
                sjf.calcularTiemposSJF();
                this.txtAreasjf.setText(sjf.listar());
            }
    }//GEN-LAST:event_btnIngresarActionPerformed


